// ─── Desktop Build Download Endpoint ─────────────────────────────
// Streams large ZIP files (80-150MB) generated by electron-packager
app.get("/api/builds/desktop-download/:token", async (req, res) => {
  try {
    const { desktopDownloadMap } = await import("./hayo/router.js");
    const entry = desktopDownloadMap.get(req.params.token);
    if (!entry) { res.status(404).json({ error: "الرابط غير صالح أو منتهي الصلاحية" }); return; }
    if (Date.now() > entry.expiresAt) {
      desktopDownloadMap.delete(req.params.token);
      res.status(410).json({ error: "انتهت صلاحية الرابط" }); return;
    }
    if (!fs.existsSync(entry.zipPath)) { res.status(404).json({ error: "الملف غير موجود" }); return; }
    const stat = fs.statSync(entry.zipPath);
    res.setHeader("Content-Type", "application/zip");
    res.setHeader("Content-Disposition", `attachment; filename*=UTF-8''${encodeURIComponent(entry.filename)}`);
    res.setHeader("Content-Length", stat.size);
    const stream = fs.createReadStream(entry.zipPath);
    stream.pipe(res);
  } catch (err: any) {
    res.status(500).json({ error: "فشل التحميل" });
  }
});

